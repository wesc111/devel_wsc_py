# Simple Python script demonstrating basic arithmetic operations

print('Hello world, let\'s do some math!')
operand1 = 127
operand2 = 23
print(f"The sum of {operand1} and {operand2} is {operand1 + operand2}")
print(f"The product of {operand1} and {operand2} is {operand1 * operand2}")
print(f"The difference of {operand1} and {operand2} is {operand1 - operand2}")
print(f"The quotient of {operand1} and {operand2} is {operand1 / operand2}")